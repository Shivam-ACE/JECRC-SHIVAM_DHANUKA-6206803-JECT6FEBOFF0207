# Department-wise Patient Count System

class Solution:

    def department_patient_count(self, visits):
        dept_count = {}

        for visit in visits:
            dept = visit["department"]
            if dept in dept_count:
                dept_count[dept] += 1
            else: 
                dept_count[dept] = 1

        max_department = ""
        max_patients = 0
        for dept in dept_count:
            if dept_count[dept] > max_patients:
                max_patients = dept_count[dept]
                max_department = dept

        return dept_count, max_department